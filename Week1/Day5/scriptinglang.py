# You can write SCRIPTS with scripting language (SL) - It is a series of commands that are run one by one

# SL is associated with web development

# SL is divided into 2 categories: SERVER SIDE AND CLIENT SIDE

# SERVER - create scripts that are run on the server side, which minimizes the workload of the browser. Some common SL are Python, PHP, Ruby, and Perl.

# CLIENT - create scripts that are run on the client side, and these are sent from the server by SERVER SIDE SL. Examples: JavaScript, jQuery, CSS, etc. 