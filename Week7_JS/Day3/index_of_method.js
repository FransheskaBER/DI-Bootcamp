// Syntax
str.indexOf(substr, startIndex)

// str = string in which substring "substr" is searched
// substr = substring whose position is searched in given str
// startIndex = starting index for the search. if not startIndex is provided, the search starts at 0 index
//  return  value = returns the start index of the first occurrance of the substr in str or returns -1 (no occurrence found)

//  the indexOf method is CASE-SENSITIVE

console.log('To be or not to be'.indexOf('To'));